package functions;

public interface TabulatedFunction extends MathFunction {

    /**
     * Возвращает количество табулированных значений
     * @return количество точек в таблице
     */
    int getCount();

    /**
     * Возвращает значение аргумента x по указанному индексу
     * @param index индекс значения (от 0 до getCount()-1)
     * @return значение x в точке с указанным индексом
     */
    double getX(int index);

    /**
     * Возвращает значение функции y по указанному индексу
     * @param index индекс значения (от 0 до getCount()-1)
     * @return значение y в точке с указанным индексом
     */
    double getY(int index);

    /**
     * Устанавливает значение функции y по указанному индексу
     * @param index индекс значения (от 0 до getCount()-1)
     * @param value новое значение y
     */
    void setY(int index, double value);

    /**
     * Возвращает индекс аргумента x в таблице
     * @param x значение аргумента для поиска
     * @return индекс x в таблице или -1, если не найден
     */
    int indexOfX(double x);

    /**
     * Возвращает индекс первого вхождения значения y в таблице
     * @param y значение функции для поиска
     * @return индекс первого вхождения y или -1, если не найден
     */
    int indexOfY(double y);

    /**
     * Возвращает самый левый (минимальный) x в области определения
     * @return минимальное значение x
     */
    double leftBound();

    /**
     * Возвращает самый правый (максимальный) x в области определения
     * @return максимальное значение x
     */
    double rightBound();
}