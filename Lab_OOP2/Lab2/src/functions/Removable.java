package functions;

/**
 * Интерфейс для удаления точек из табулированной функции
 * Позволяет удалять точки по указанному индексу
 */
public interface Removable {

    /**
     * Удаляет точку из табулированной функции по указанному индексу
     *
     * @param index индекс точки для удаления (от 0 до getCount()-1)
     *
     * Правила удаления:
     * - После удаления количество точек уменьшается на 1
     * - Индексы последующих точек сдвигаются
     * - Если после удаления остается меньше 2 точек, поведение должно быть определено
     *   в реализации (может бросать исключение или иметь специальную логику)
     *
     * @throws IllegalArgumentException если индекс некорректен
     * @throws IllegalStateException если удаление нарушает целостность функции
     */
    void remove(int index);
}
