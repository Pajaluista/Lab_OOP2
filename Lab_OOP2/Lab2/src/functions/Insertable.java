package functions;

/**
 * Интерфейс для вставки значений в табулированную функцию
 * Позволяет добавлять или обновлять точки в таблице функции
 */
public interface Insertable {

    /**
     * Вставляет или обновляет значение в табулированной функции
     *
     * @param x значение аргумента
     * @param y значение функции
     *
     * Правила вставки:
     * - Если x уже есть в таблице: заменяет соответствующее значение y
     * - Если x между существующими значениями: вставляет новую точку между ними
     * - Если x левее всех значений: добавляет в начало таблицы
     * - Если x правее всех значений: добавляет в конец таблицы
     */
    void insert(double x, double y);
}